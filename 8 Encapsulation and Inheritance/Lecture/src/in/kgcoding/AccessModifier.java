/*
<--------------------------------------------------{"Using Access-Modifier in Deep"}--------------------------------------------------------------------->

1.Definition:-------------> Access modifiers are keywords which define the accessibility of a class and its members.
                            Access modifiers are used in Java to control the visibility of classes, interfaces, variables,
                            methods, constructors, data members, and setter methods.
2.Types:------------------> The four access modifiers are public, protected, default(no modifier), and private.
3.Public:-----------------> The public modifier allows access from any other class. For classes, it means they can be accessed from any other package.
4.Protected:--------------> The protected modifier allows access within the same package and subclass.
5.Default:----------------> If no access modifier is specified, it's the default, allowing access only within the same package.
6.Private:----------------> The private modifier restricts access to the defining class only.
7.Class-Level Access:-----> Only public and default (no modifier) access modifiers are applicable for top-level classes.
8.Member-Level Access:----> Methods, Constructors, and Variables can use all four access modifiers to control visibility.


 */